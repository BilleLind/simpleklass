class Game {

    String name;
    int ageOfGame;
    int copiesSold;
    String company;

    public int getAge() {
        return 2019-ageOfGame;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAgeOfGame() {
        return ageOfGame;
    }

    public void setAgeOfGame(int ageOfGame) {
        this.ageOfGame = ageOfGame;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public int getCopiesSold() {
        return copiesSold;
    }

    public void setCopiesSold(int copiesSold) {
        this.copiesSold = copiesSold;
    }


}
